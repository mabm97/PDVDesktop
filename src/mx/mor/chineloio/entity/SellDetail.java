package mx.mor.chineloio.entity;

/**
 *
 * @author MarcoABM
 */
public class SellDetail {

    private String id;
    private String cantidad;
    private String descripcion;
    private String precio;
    private String importe;
    private String estatus;
    private String sellId;
    private String presentationId;
    private String createdAt;
    private String updatedAt;

    public SellDetail() {
    }

    public SellDetail(String id, String cantidad, String descripcion, String precio, String importe, String estatus, String sellId, String presentationId, String createdAt, String updatedAt) {
        this.id = id;
        this.cantidad = cantidad;
        this.descripcion = descripcion;
        this.precio = precio;
        this.importe = importe;
        this.estatus = estatus;
        this.sellId = sellId;
        this.presentationId = presentationId;
        this.createdAt = createdAt;
        this.updatedAt = updatedAt;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCantidad() {
        return cantidad;
    }

    public void setCantidad(String cantidad) {
        this.cantidad = cantidad;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getPrecio() {
        return precio;
    }

    public void setPrecio(String precio) {
        this.precio = precio;
    }

    public String getImporte() {
        return importe;
    }

    public void setImporte(String importe) {
        this.importe = importe;
    }

    public String getEstatus() {
        return estatus;
    }

    public void setEstatus(String estatus) {
        this.estatus = estatus;
    }

    public String getSellId() {
        return sellId;
    }

    public void setSellId(String sellId) {
        this.sellId = sellId;
    }

    public String getPresentationId() {
        return presentationId;
    }

    public void setPresentationId(String presentationId) {
        this.presentationId = presentationId;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public String getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(String updatedAt) {
        this.updatedAt = updatedAt;
    }

}
